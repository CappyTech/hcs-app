#!/usr/bin/env node
/**
 * Extract class attribute values from all EJS templates and emit a tailwind.safelist.js
 * Only needed for classes that Tailwind cannot see (dynamic/injected). Normal static classes
 * inside scanned content paths are already picked up; this is defensive for future changes.
 */
import fg from 'fast-glob';
import fs from 'fs';
import path from 'path';

const ROOT = process.cwd();
const VIEW_GLOBS = ['mongoose/views/**/*.ejs'];

function extractClasses(content) {
  const classes = new Set();
  const classAttrRegex = /class\s*=\s*(["'])(.*?)\1/gs; // capture quoted value
  let match;
  while ((match = classAttrRegex.exec(content))) {
    const raw = match[2];
    // Skip EJS interpolation fragments
    if (!raw || /<%/.test(raw)) continue;
    raw.split(/\s+/).forEach(token => {
      if (!token) return;
      // Basic filtering: ignore arbitrary value brackets since Tailwind handles them directly (e.g., w-[360px])
      // Keep anything that looks like a tailwind utility pattern
      classes.add(token.trim());
    });
  }
  return classes;
}

(async () => {
  try {
    const files = await fg(VIEW_GLOBS, { cwd: ROOT, absolute: true });
    const all = new Set();
    for (const file of files) {
      try {
        const content = fs.readFileSync(file, 'utf8');
        const cs = extractClasses(content);
        cs.forEach(c => all.add(c));
      } catch (e) {
        console.warn('Failed reading', file, e.message);
      }
    }

    // Filter out obviously non-tailwind tokens (heuristic)
    const filtered = Array.from(all).filter(c => {
      // Exclude long JSON-like strings or ones containing ':' (except pseudo variants) or '='
      if (c.length > 80) return false;
      if (/=/.test(c)) return false;
      return true;
    }).sort();

    const outPath = path.join(ROOT, 'tailwind.safelist.js');
    const banner = '// Auto-generated by scripts/generate-tailwind-safelist.js; DO NOT EDIT MANUALLY\n';
    const body = `export default ${JSON.stringify(filtered, null, 2)};\n`;
    fs.writeFileSync(outPath, banner + body, 'utf8');
    console.log(`Generated safelist with ${filtered.length} classes -> tailwind.safelist.js`);
  } catch (err) {
    console.error('Safelist generation failed:', err);
    process.exit(1);
  }
})();
